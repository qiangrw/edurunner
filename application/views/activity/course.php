<h3>认识世界</h3>
<p>
在学生掌握一定程度的认识自我和认识他人的知识技能基础上，“认识世界”板块以学习与实践结合的形式，加强其与外部世界的互动。学生通过社会实践前的活动设计，团队活动预演和社会现场实践走出课堂，体验社会，触摸世界的不同层面。
</p>
