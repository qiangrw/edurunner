</p>

<h3>认识他人，学会沟通尊重，培养团队精神</h3>
<p>
认识他人是个人融入社会获得进一步发展的必经之路，对认识自己也有促进作用。学会观察，沟通和尊重，能让我们在团队中更愉快。在“认识他人”板块，导师通过互动游戏、案例分析、团队活动实践等，引导同学思考和学习与人沟通和协作的技巧。导师将引导同学对以《三打白骨精》为蓝本的案例进行团队合作分析，引导学生意识到团队中有效沟通、相互理解和尊重、分工合作、分享共同愿景的重要性。在熟悉团队合作的基本原则之后，同学在导师的指导下，以小组为单位自行设计一个为期三个小时的社区服务项目。
</p>

