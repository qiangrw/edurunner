<h2>课程理念</h2>

<h3>认识自我，培养主动心理，实现精彩人生i</h3>
<p>
自我是个人成长的根基，也是影响个人认识他人与认识世界的重要因素。我们提倡个人深化与拓展对自己的认识，了解自己的价值与兴趣，并拥抱积极主动的人生态度，探索并走出属于自己的精彩人生之路。即使平凡也可以创造出让人羡慕的成就。
</p>
<p>
在“认识自我”板块，导师通过互动课堂、个人故事分享等引导同学认识自己、挖掘自己的优点、设立个人目标并为目标制定实践计划。下图展现了本部分课程的思路：
</p>

