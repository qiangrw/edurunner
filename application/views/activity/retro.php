<img class="pic" src="images/retro7.jpg" width=500></img>
</p>
<p>
 2012年“彩虹行动”新市民子弟多彩夏令营导师：“去年彩虹行动的第二天收集上来的学生反馈中一句‘经过这两天的学习，我发现自己并没有那么糟糕’让我有了很大的动力和决心把彩虹当成我的事业。如今，每每我想起这句话都会很有感触，而这种感触我想借用游子甘肃行的口号来形容——‘假如我能使一颗心免于受伤’。”
</p>

<p>
 2012年“彩虹行动”新市民子弟多彩夏令营志愿者：“这是我第一次参加志愿者活动，我把我的好多第一次都献给了彩虹。年龄小，阅历少，让我很多方面都处于很被动的状态，就连自我介绍都做不好。可是我的同伴们没有嫌弃我，大家都对我很好，会对我提点，给予我帮助，让我感受到了志愿者团队的温暖。”
</p>

