        <div class="box">
            <h3>彩虹行动</h3>
            <p> 
            彩虹行动是“教育先行”的主打活动, 其目标对象是城市中的新市民（外来务工者）的子女。“彩虹行动”夏令营为期四天，从认识自我、认识他人、认识世界三个目标出发，围绕人生规划、时间管理、团队合作、批判性思维等主题开展课程和活动，通过互动课堂、团队合作游戏和实践社区服务项目等形式提高新市民子弟的自信心和创造力，增强自我意识，增进团队合作精神。
            </p>
