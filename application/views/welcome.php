				<h2>EduRunner</h2>
				<p>教育先行（EduRunner），成立于2011年，是一个由留美学子创立的、在美国注册的非营利组织。我们关注教育公平，致力于青少年个人发展的咨询和培训。通过提供与能力建设相关的培训课程、工作坊等活动，促进青少年个性发展，提高他们团队合作和社会参与的能力，旨在通过教育理论研究和教育实践关注中国教育改革实践和新市民子弟。
				</p>
				<p>目前教育先行的主要活动是每年一度的新市民子弟彩虹行动夏令营。</p>
