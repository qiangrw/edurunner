            <h3>EduRunner彩虹行动成长夏令营志愿者招募</h3>
            <p>
            抬头仰望星空，低头汗水挥洒大地。每个在路上的人，心里都有着最深沉的爱，从不需繁琐的言语，因为一直在用行动来证明对于这块大地的执着。在这似水的年华里，我想与这样一群人分享我的爱，我的泪和我的汗。
            </p>
            <p>
            这个夏天，EduRunner彩虹行动成长夏令营，来到北京蒲公英中学、浙江三门县，给这里的孩子带来个性发展、公民课程和公益社会实践课程的成长夏令营。
            （<a href=<?php echo site_url('support/volunteer') ?>>阅读更多</a>）
            <p>

            <p>
            目前，教育先行的主打活动是“彩虹行动”----新市民子弟多彩夏令营。
            </p>
