<h3>志愿者团队（排名按姓名首字母顺序）</h3>

<h4>1.陈晓中</h4>
<p>
毕业于哈佛大学，主修国际教育政策，本科毕业于加州大学洛杉矶分校历史专业。北戴河启行营地发展中心项目部主管。曾在香港岭南大学服务研习处任导师，北京大学中国教育财政科学研究所任科研助理，一直热心教育公益。
</p>
 
<h4>2.林舒</h4>
<p>
现于麦肯锡中国城市计划任职。硕士毕业于哈佛大学，专修教育学，研究方向是人类发展与心理学。本科毕业于上海外国语大学。曾在哈佛大学教育学院担任研究助理，并参与由联合国儿童基金会出资的幼儿教育系统研究项目。从高中时代支教开始，便一直希望能让更多孩子得到更好的学习机会。
</p>

<p>
林舒于2011年共同创建了 “教育先行”。
</p>
		  
<h4>3.汤凯程</h4>
<p>
现就读于中国人民大学商学院会计系，曾参加美国乔治城大学暑期学校。课余时间积极参与各类志愿者活动，并专注于教育和公共管理领域。她希望把自己的所拥有的学习方法以及积极乐观的人生态度与更多人分享。

<h4>4.欧阳晨曦</h4>
<p>
现就读于美国波士顿大学，主修心理学和电影。曾在湖南乡村支教三个月，学生的聪明好学、善良淳朴深深触动了她。上大学后，认识了许多志同道合的朋友，从而有机会参与“彩虹行动”，见证她的成长，以及她为学生们带来的积极影响。“彩虹是同伴，是大家汗水灌溉出来的果实，希望有更多朋友加入彩虹，用自己的长处，浇灌彩虹，使她绽放出更耀眼的光芒。”
</p>

<h4>5.薛蕙华</h4>
<p>
现就读于江苏大学MBA。大学期间，组织并参加了旨在帮助贫困儿童及白血病患者的志愿者服务项目。“彩虹行动”导师们的博爱理想、营员们的热情活泼深深触动了她，从此坚定了她为彩虹增添一份力量的愿望。
</p>
	
				
