<h3>志愿者团队（排名按姓名首字母顺序）</h3>
<h4>1.程栩楠</h4>
<p>
现就读于美国波士顿大学，主修经济数学双学位；波士顿大学中国学生学者联合会市场部部长。她希望尽自己的绵薄之力帮助有需要的人，希望自己可以在活动中跟孩子们一起进步，也希望有更多的人加入彩虹，关注新市民子弟教育。
</p>

<h4>2.樊双梓</h4>
<p>
现就读于深圳大学经济学院。热爱志愿者服务活动，曾加入“大运会乒乓球测试赛—2011中国乒乓球公开赛”志愿者队伍，并成为了“第十届广东省中学生运动会”的志愿者。2012年加入彩虹行动的团队中，成为了彩虹的一员。“短短的四天夏令营，不仅从团队的同事们身上学到了很多，也从孩子们的身上学到了很多。看到孩子们脸上自信的神情，听到他们表达自己内心的感受，是我觉得最幸福的时刻。”
</p>

<h4>3.黄圣莺</h4>
<p>
现就读于越秀外国语学院西语学院法语系。西语学院文艺部副部长，曾任心理社社长。她坚信只有在历练中，才能获得成长。“我没有过很多的志愿者经历，但我愿意为彩虹付出我的绵薄之力。”希望在“彩虹行动”中，和志同道合的同伴们一起帮助有需要的孩子们。
</p>

<h4>4.薛蕙华</h4>
<p>
现就读于江苏大学MBA。大学期间，组织并参加了旨在帮助贫困儿童及白血病患者的志愿者服务项目。“彩虹行动”导师们的博爱理想、营员们的热情活泼深深触动了她，从此坚定了她为彩虹增添一份力量的愿望。
</p>
