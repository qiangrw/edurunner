<h4>3.李越欣</h4>
<p >
      李越欣，现为中国人民大学经济学院本科生。一直积极参与志愿活动，尤其活跃于支教和临终关怀活动。在志愿活动中，与人交流，分享故事，是人生一大乐事。感谢彩虹行动让我有机会认识到了一群可爱的、充满理想的人，在分享中获得超越。
</p>

<h4>4.欧阳晨曦</h4>
<p>
      欧阳晨曦，现为美国波士顿大学大三学生，主修心理学和电影。我十八岁那年去湖南乡村支教三个月，学生们的聪明好学，善良淳朴深深打动了我。上大学后，认识了许多志同道合的朋友，这使得我有机会参与彩虹，见证她的成长，以及她为学生们带来的积极影响。彩虹是同伴，是大家汗水灌溉出来的果实，希望有更多朋友加入彩虹，用自己的长处，浇灌彩虹，使她绽放出更耀眼的光芒。
</p>

<h4>5.单丹阳</h4>
<p>
       单丹阳，现就读于中国人民大学劳动人事学院2010级劳动关系专业，是个地地道道的东北女孩。本人热爱公益事业，进入大学后便加入了学校青年志愿者协会支教项目部，开始了支教之旅，至今已有两年时间。我认为支教的意义并不在于交给学生什么，更重要的是为他们搭建一个认识五彩斑斓的世界的舞台，是要走进他们的内心世界。
</p>
<p >
       加入彩虹为我的志愿活动加入了浓墨重彩的一笔，我希望未来的彩虹行动能让人更多的外来务工子女融入进来，也希望有更多的志愿者能够加入彩虹，贡献出自己的一份光，一份热。
</p>

<h4>6.汤凯程</h4>
<p>
       <strong>汤凯程</strong>，现就读于<a href="http://www.ruc.edu.cn/home1024" target="_blank">中国人民大学</a>商学院会计系本科。曾参加美国乔治城大学暑期学校。课余时间积极参与学院举办的志愿者活动，并对教育和公共管理等领域非常感兴趣。2011年暑假机缘巧合之下参加了教育先行<span style="color:red">“彩虹行动”</span>新市民子弟多彩夏令营，夏令营学生的反馈中一句<em>“经过这两天的学习，我发现自己没有那么糟糕”</em>深深打动了我，并让我决心把彩虹当成是我的事业。我希望把自己的所拥有的学习方法以及积极乐观的人生态度与更多人分享。
</p>

<h4>7.徐珺泽</h4>
<p >
       徐珺泽，波士顿大学幼儿教育硕士，“终身学习” 和“E-Learning"的实践者。专注于儿童教育的同时，也关注教育和社会公益、科技、创业等领域的联系。笃信积极乐观的生活态度，踏实而不懈的行动和不断的探寻自我、追逐不囿于自我的梦想是人生幸福的几个重要元素，并把这个信念结合到了给“彩虹行动”设计的活动方案中。喜欢艺术，游戏，自然和创意，也希望“彩虹”的孩子们都能用多种方式自如地表达自己。
</p>

