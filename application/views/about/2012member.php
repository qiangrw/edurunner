<h3>志愿者团队（排名按姓名首字母顺序）</h3>
<h4>1.程栩楠</h4>
<p>
       程翊楠，就读于美国波士顿大学，美国波士顿大学中国学生学者联合会市场部部长。是个经济，数学双学位本科在读的理学院学生。一直希望尽自己的绵薄之力帮助有需要的人，所以参加了这次彩虹活动夏令营，也想以后能找到更多的类似的义工活动，更多，更好，更高效地服务社会。
</p>
<p>
       以往参加的一些活动，最终不过如日月无声，水过无痕般的就结束了，但彩虹却像一个大家庭，给了我点亮了一小株可以燎原般星星之火。记得回程的火车上，漫天的雨点打在车窗，想到孩子们，队友们，不禁就红了眼眶。
</p>
<p>
       二十出头本就该是个年轻，在路上且热泪盈眶的年纪。为何不乘着青春多接触社会的方方面面呢？我愿继续参加彩虹活动，希望自己可以在活动中跟孩子们一起进步，也希望有更多的人加入彩虹，关注外来务工人员子女教育。愿今日的你我可以在明年重复今年夏天的故事，以己之力在尘埃中开花。
</p>

<h4>2.樊双梓</h4>
<p>
       樊双梓，现就读于深圳大学经济学院。热爱志愿者服务活动，于2011年7月加入“大运会乒乓球测试赛—2011中国乒乓球公开赛”志愿者队伍，2012年7月再次成为了“第十届广东省中学生运动会”的志愿者。2012年8月，加入彩虹行动的团队中，成为了彩虹的一员。短短的四天夏令营，不仅从团队的同事们身上学到了很多，也从孩子们的身上学到了很多。看到孩子们脸上自信的神情，听到他们表达自己内心的感受，是我觉得最幸福的时刻。
</p>

<h4>3.黄圣莺</h4>
<p>
       黄圣莺，现就读于浙江越秀外国语学院西语学院法语系。曾任阳光心理社社长，获得过“优秀共青团干部”的称号。 我的性格比较外向，在大家眼中我是个开朗乐观的女生，能和周围人友好快乐的相处。在学校我总是积极地参与课外活动，包括很多志愿者行动。我很享受帮助别人的感觉，这不仅让别人快乐，更让我感受到幸福。我认为，每个人生活在社会这个大家庭中，就需要与周围的人和谐相处。2012年暑假我加入了彩虹，在这里我遇到了这群可爱的孩子们，我愿意帮助他们，把温暖带给他们！
</p>

