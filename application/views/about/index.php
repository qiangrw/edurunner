    <h2>组织介绍</h2>
    <p>
    教育先行（EduRunner）成立于2011年，是一个由留美学子创立的、在美国注册的公益组织。我们关注教育公平，致力于青少年个人发展的咨询和培训。通过提供与能力建设相关的培训课程、工作坊等活动，促进青少年个性发展，提高他们团队合作和社会参与的能力。
    </p>

    <img src="images/logo.jpg" class="pic" alt="组织LOGO" titile="EduRunner"></img>

    <p>
    目前，教育先行的主打活动是“彩虹行动”新市民子弟多彩夏令营。
    </p>

    <h3>我们的信念</h3>
    <p>我们相信有才艺、有梦想、有追求的青少年都能找到自己的成长之路。 </p>
    <p>我们相信通过开展能力建设和成长教育课程，能推动建设更公平、更幸福的社会。 </p>

