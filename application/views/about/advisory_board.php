<h2>顾问委员会</h2>
<p>
<b>专家顾问委员</b>会由国内外教育学、公共管理领域专家组成。专家顾问团队为 “教育先行”课程设计、未来发展提供咨询与指导，并提供内部能力建设培训。
</p>
<p>
<b>青年顾问委员会</b>由“教育先行”夏令营往期成员、活跃在教育和公益领域的青年领袖组成。青年顾问委员会参与“彩虹行动”夏令营及年会，为教育先行课程设计、未来发展提供咨询与指导，并提供内部能力建设培训。
</p>

